    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        // TODO add your handling code here:
        String name = "", author = "", publishDate = "";
        int seriNo = 0;

        if (!jTextFieldName.getText().isEmpty()) {
            name = jTextFieldName.getText();
        } else {
            JOptionPane.showMessageDialog(this, "Please enter a valid name!");
            return;
        }

        if (!jTextFieldAuthor.getText().isEmpty()) {
            author = jTextFieldAuthor.getText();
        } else {
            JOptionPane.showMessageDialog(this, "Please enter a valid surename!");
            return;
        }
        if (!jTextFieldPubDate.getText().isEmpty()) {
            publishDate = jTextFieldPubDate.getText();
        } else {
            JOptionPane.showMessageDialog(this, "Please enter a valid surename!");
            return;
        }
        if (!jLabelSeriNo.getText().isEmpty()) {
            seriNo = Integer.parseInt(jLabelSeriNo.getText());
        } else {
            JOptionPane.showMessageDialog(this, "Please enter a valid serial number!");
            return;
        }
        for (Book book : Management.books) {
            if (book.seriNo == seriNo) {
                book.name = name;
                book.author = author;
                book.PublishDate = publishDate;
            }
        }
        JOptionPane.showMessageDialog(this, "Book had been updated successfully!");
        new Management().setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_jButtonUpdateActionPerformed

