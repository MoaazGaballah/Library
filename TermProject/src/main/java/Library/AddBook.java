    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        // TODO add your handling code here:
        String name = "", auther = "", publishDate = "";
        int seriNo = 0;

        if (!jTextFieldName.getText().isEmpty()) {
            name = jTextFieldName.getText();
        } else {
            JOptionPane.showMessageDialog(this, "Please enter a valid name!");
            resetFields();
            return;
        }

        if (!jTextFieldAuther.getText().isEmpty()) {
            auther = jTextFieldAuther.getText();
        } else {
            JOptionPane.showMessageDialog(this, "Please enter a valid surename!");
            resetFields();
            return;
        }
        if (!jTextFieldPubDate.getText().isEmpty()) {
            publishDate = jTextFieldPubDate.getText();
        } else {
            JOptionPane.showMessageDialog(this, "Please enter a valid surename!");
            resetFields();
            return;
        }
        try {
            seriNo = Integer.parseInt(jTextFieldSeriNo.getText());
            if (!searchForSeriNo(seriNo)) {
                JOptionPane.showMessageDialog(this, "This serial number had been used before!");
                resetFields();
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid number!");
            jTextFieldSeriNo.setText("");
            return;
        }
        Management.books.add(new Book(name, auther, publishDate, seriNo));
        JOptionPane.showMessageDialog(this, "Book had been added successfully!");
        resetFields();
        new Management().setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_jButtonAddActionPerformed

